    public boolean verificaDados() {
        if (!jnome.getText().equals("") && !jendereco.getText().equals("") && !jtelefone.getText().equals("")) {
            
            return true;
        } else {
            
            JOptionPane.showMessageDialog(null, "Usuario voce esqueceu de prencher algum campo");
            return false;
        }
    }
    
    private void jTtabelaLinhaSelecionada(JTable tabela) {
        if (jTtabela.getSelectedRow() != -1) {
            jid.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getId()));
            jnome.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getNome()));
            jtelefone.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getTelefone()));
            jendereco.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getEndereco()));
            jemail.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getEmail()));
            jsexo.setText(String.valueOf(contatos.get(tabela.getSelectedRow()).getSexo()));
        } else {
            jid.setText("");
            jnome.setText("");
            jendereco.setText("");
            jtelefone.setText("");
            jemail.setText("");
            jsexo.setText("");
            
        }
    }

